public class Main {
    public static void main(String[] args) {
        for (int nums1 = 0; nums1 <= 30; nums1++) {
            System.out.print(nums1 + " ");
        }

        System.out.println();

        for (int nums2 = 30; nums2 >= 0; nums2--) {
            System.out.print(nums2 + " ");
        }

        for (int i = 0; i <= 18; i += 3) {
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i = 10; i >= 0; i -= 2) {
            System.out.print(i + " ");
        }

        System.out.println();
    }
}